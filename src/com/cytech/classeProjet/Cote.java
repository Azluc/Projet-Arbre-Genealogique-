package com.cytech.classeProjet;

/**
 * Enumeration defining the possible sides for a person in the genealogical tree.
 * The side indicates which part of the family a person belongs to.
 */
public enum Cote {
	/** Maternal side of the family */
	MATERNEL,
	
	/** Paternal side of the family */
	PATERNE,
	
	/** Neutral side (used for the root person of the tree) */
	NEUTRE
}